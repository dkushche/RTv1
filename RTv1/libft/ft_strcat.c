char			*ft_strcat(char *dest, const char *append)
{
	char		*p;
	const char	*a;

	a = append;
	p = dest;
	while (*p != '\0')
	{
		p = p + 1;
	}
	while (*a != '\0')
	{
		*p = *a;
		p = p + 1;
		a = a + 1;
	}
	*p = '\0';
	return (dest);
}
